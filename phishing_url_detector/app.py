class PDFReport(FPDF):
    def clean_text(self, text):
        """Sanitize text for Latin-1 encoding (replace unsupported chars)"""
        return str(text).encode('latin-1', 'replace').decode('latin-1')

    def header(self):
        # Logo or Title styling
        self.set_font('Arial', 'B', 20)
        self.set_text_color(33, 37, 41)  # Dark Gray
        self.cell(0, 10, 'Phishing Detection Security Report', 0, 1, 'L')
        
        # Decorative Line
        self.set_line_width(0.5)
        self.set_draw_color(0, 217, 255) # Cyan Primary
        self.line(10, 22, 200, 22)
        self.ln(15)
        
    def footer(self):
        self.set_y(-15)
        self.set_font('Arial', 'I', 8)
        self.set_text_color(128, 128, 128) # Gray
        self.cell(0, 10, f'Page {self.page_no()} | Generated by Phishing Detection System', 0, 0, 'C')

    def chapter_title(self, title):
        self.set_font('Arial', 'B', 14)
        self.set_fill_color(24, 29, 41) # Dark Blue/Black Background
        self.set_text_color(0, 217, 255) # Cyan Text
        self.cell(0, 10, f"  {title}", 0, 1, 'L', fill=True)
        self.ln(4)

    def section_kv(self, key, value, new_line=True):
        self.set_font('Arial', 'B', 10)
        self.set_text_color(50, 50, 50)
        self.cell(50, 6, f"{key}:", 0, 0)
        self.set_font('Arial', '', 10)
        self.set_text_color(0, 0, 0)
        self.multi_cell(0, 6, self.clean_text(str(value)))
        if new_line:
            self.ln(2)

def load_model_components():
    """Load all model components at startup"""
    global url_model, email_model, url_vectorizer, email_vectorizer, model_info, email_model_info
    
    try:
        models_loaded = 0
        
        # Load URL model
        phishing_model_path = os.path.join(MODEL_DIR, 'phishing_model.h5')
        if os.path.exists(phishing_model_path):
            print("🔄 Loading URL model...")
            url_model = tf.keras.models.load_model(phishing_model_path, custom_objects={
                'MultiHeadSelfAttention': MultiHeadSelfAttention
            })
            url_vectorizer = joblib.load(os.path.join(MODEL_DIR, 'vectorizer.pkl'))
            
            model_info_path = os.path.join(MODEL_DIR, 'model_info.pkl')
            if os.path.exists(model_info_path):
                model_info = joblib.load(model_info_path)
            
            print("✅ URL model loaded successfully!")
            models_loaded += 1
        
        # Load Email model
        email_model_path = os.path.join(MODEL_DIR, 'email_model.h5')
        if os.path.exists(email_model_path):
            print("🔄 Loading Email model...")
            email_model = tf.keras.models.load_model(email_model_path, custom_objects={
                'MultiHeadSelfAttention': MultiHeadSelfAttention
            })
            email_vectorizer = joblib.load(os.path.join(MODEL_DIR, 'email_vectorizer.pkl'))
            
            email_model_info_path = os.path.join(MODEL_DIR, 'email_model_info.pkl')
            if os.path.exists(email_model_info_path):
                email_model_info = joblib.load(email_model_info_path)
            
            print("✅ Email model loaded successfully!")
            models_loaded += 1
        
        if models_loaded == 0:
            print("❌ No models found! Please run 'python train_model.py' first.")
            return False
         
        print(f"✅ {models_loaded}/2 models loaded successfully!")
        return True
        
    except Exception as e:
        print(f"❌ Error loading models: {e}")
        return False

