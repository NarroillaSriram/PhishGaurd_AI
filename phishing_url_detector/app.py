@app.route('/download_report/<scan_id>')
def download_report(scan_id):
    """Generate and download a dynamic PDF report for a specific scan"""
    scan_data = next((item for item in scan_history if item['id'] == scan_id), None)
    
    if not scan_data:
        return jsonify({'error': 'Scan not found'}), 404
        
    try:
        # Create PDF
        pdf = PDFReport()
        pdf.set_auto_page_break(auto=True, margin=15)
        pdf.add_page()
        
        # --- METADATA ---
        pdf.set_font('Arial', '', 10)
        pdf.set_text_color(100, 100, 100)
        pdf.cell(0, 5, f"Report ID: {scan_data['id']}", 0, 1, 'L')
        pdf.cell(0, 5, f"Date: {scan_data['timestamp']}", 0, 1, 'L')
        pdf.ln(5)

        # --- EXECUTIVE SUMMARY ---
        pdf.chapter_title("Executive Summary")
        
        # Verdict
        prediction = scan_data.get('prediction', 'Unknown')
        confidence = scan_data.get('confidence', 0)
        
        pdf.set_font('Arial', 'B', 12)
        pdf.cell(40, 8, "Verdict:", 0, 0)
        
        if prediction == "Phishing":
            pdf.set_text_color(220, 53, 69) # Red
        elif prediction == "Suspicious":
             pdf.set_text_color(255, 193, 7) # Orange
        else:
            pdf.set_text_color(40, 167, 69) # Green
            
        pdf.cell(0, 8, prediction.upper(), 0, 1)
        pdf.set_text_color(0, 0, 0) # Reset
        
        pdf.set_font('Arial', '', 10)
        pdf.cell(40, 6, "Confidence:", 0, 0)
        pdf.cell(0, 6, f"{confidence}%", 0, 1)
        pdf.cell(40, 6, "Recommendation:", 0, 0)
        pdf.multi_cell(0, 6, pdf.clean_text(scan_data.get('recommendation', 'N/A')))
        pdf.ln(4)
        
        # --- ANALYSIS ---
        pdf.chapter_title("Input Analysis")
        pdf.section_kv("Analyzed Input", scan_data.get('input', 'N/A'))
        pdf.section_kv("Type", scan_data.get('type', 'Unknown').upper())
        
        dom_info = scan_data.get('domain_info', {})
        if dom_info and scan_data.get('type') == 'URL':
            pdf.ln(2)
            pdf.set_font('Arial', 'B', 10)
            pdf.cell(0, 6, "Domain Intelligence:", 0, 1)
            pdf.set_font('Arial', '', 10)
            pdf.section_kv("  - Establishment", dom_info.get('creation_date', 'Unknown'), new_line=False)
            pdf.section_kv("  - Age", f"{dom_info.get('domain_age_days', 0)} days", new_line=False)
            pdf.section_kv("  - Registrar", dom_info.get('registrar', 'Unknown'), new_line=True)

        pdf.ln(2)
        
        # --- SCORING BREAKDOWN ---
        pdf.chapter_title("Risk Assessment Scores")
        scores = scan_data.get('scores', {})
        
        def draw_score_bar(label, score):
            pdf.set_font('Arial', '', 10)
            pdf.cell(60, 6, label, 0, 0)
            
            # Draw bar background
            x = pdf.get_x()
            y = pdf.get_y()
            pdf.set_fill_color(230, 230, 230)
            pdf.rect(x, y+1, 80, 4, 'F')
            
            # Draw actual score
            if score > 70: pdf.set_fill_color(220, 53, 69)
            elif score > 40: pdf.set_fill_color(255, 193, 7)
            else: pdf.set_fill_color(40, 167, 69)
            
            bar_width = (score / 100) * 80
            if bar_width > 0:
                pdf.rect(x, y+1, bar_width, 4, 'F')
                
            pdf.set_x(x + 85)
            pdf.cell(20, 6, f"{score}/100", 0, 1)

        draw_score_bar("AI Transformer Model", scores.get('transformer', 0))
        draw_score_bar("Heuristic Rules", scores.get('rule_based', 0))
        draw_score_bar("Domain Risk Profile", scores.get('domain_risk', 0))
        draw_score_bar("Web Behavior Risk", scores.get('behavior_risk', 0))
        
        pdf.ln(2)
        pdf.set_font('Arial', 'B', 10)
        
        # Consistent with Frontend: Show Safety Score if Legitimate
        final_score = scores.get('final', 0)
        if prediction == "Legitimate":
            display_label = "FINAL SAFETY SCORE:"
            display_value = round(100 - final_score, 2)
        else:
            display_label = "FINAL RISK SCORE:"
            display_value = final_score
            
        pdf.cell(60, 8, display_label, 0, 0)
        pdf.set_font('Arial', 'B', 12)
        pdf.cell(0, 8, f"{display_value}/100", 0, 1)
        pdf.ln(4)

        # --- WEBSITE PROFILE ---
        if scan_data.get('type') == 'URL':
            pdf.chapter_title("Website Profile & Purpose Analysis")
            pdf.set_font('Arial', 'B', 10)
            pdf.set_text_color(0, 102, 204) # Blue
            pdf.cell(40, 6, "Category:", 0, 0)
            pdf.set_font('Arial', '', 10)
            pdf.set_text_color(0, 0, 0)
            pdf.cell(0, 6, scan_data.get('website_category', 'General'), 0, 1)
            
            pdf.set_font('Arial', 'B', 10)
            pdf.set_text_color(102, 0, 204) # Purple
            pdf.cell(40, 6, "Design Intent:", 0, 0)
            pdf.set_font('Arial', '', 10)
            pdf.set_text_color(0, 0, 0)
            pdf.cell(0, 6, scan_data.get('design_intent', 'N/A'), 0, 1)
            
            # Visual Trust calculation
            signals_count = len(scan_data.get('legitimacy_signals', []))
            trust_level = "High" if signals_count >= 3 else "Medium" if signals_count >= 1 else "Low/New"
            
            pdf.set_font('Arial', 'B', 10)
            pdf.set_text_color(40, 167, 69) # Green
            pdf.cell(40, 6, "Visual Trust:", 0, 0)
            pdf.set_font('Arial', '', 10)
            pdf.set_text_color(0, 0, 0)
            pdf.cell(0, 6, trust_level, 0, 1)

            pdf.ln(2)
            pdf.set_font('Arial', 'B', 10)
            pdf.cell(0, 6, "Site Utility & Purpose Summary:", 0, 1)
            pdf.set_font('Arial', '', 10)
            purpose = scan_data.get('website_purpose', 'N/A')
            # Clean summary from markdown bold stars
            purpose = purpose.replace("**", "")
            pdf.multi_cell(0, 6, pdf.clean_text(purpose))
            pdf.ln(4)

        # --- FINDINGS ---
        pdf.chapter_title("Detailed Findings")
        
        # Keywords
        words = scan_data.get('suspicious_words', [])
        pdf.set_font('Arial', 'B', 10)
        pdf.cell(0, 6, "Suspicious Keywords Detected:", 0, 1)
        pdf.set_font('Arial', '', 10)
        if words:
            pdf.multi_cell(0, 6, pdf.clean_text(", ".join(words)))
        else:
            pdf.cell(0, 6, "None detected.", 0, 1)
        pdf.ln(2)
        
        # Behavior
        findings = scan_data.get('behavior_findings', [])
        pdf.set_font('Arial', 'B', 10)
        pdf.cell(0, 6, "Behavioral Anomalies:", 0, 1)
        pdf.set_font('Arial', '', 10)
        if findings:
            for flag in findings:
                pdf.cell(5, 6, "-", 0, 0)
                pdf.multi_cell(0, 6, pdf.clean_text(str(flag)))
        else:
            pdf.cell(0, 6, "No behavioral anomalies detected/Crawler skipped.", 0, 1)
        pdf.ln(2)

        # Legitimacy Signals
        legitimacy = scan_data.get('legitimacy_signals', [])
        if legitimacy:
            pdf.set_font('Arial', 'B', 10)
            pdf.set_text_color(40, 167, 69) # Green
            pdf.cell(0, 6, "Verified Legitimacy Signals (Positive Markers):", 0, 1)
            pdf.set_font('Arial', '', 10)
            pdf.set_text_color(0, 0, 0) # Reset
            for signal in legitimacy:
                pdf.cell(5, 6, "+", 0, 0)
                pdf.multi_cell(0, 6, pdf.clean_text(str(signal)))
            pdf.ln(2)
        # Save
        folder = os.path.join(BASE_DIR, 'reports')
        os.makedirs(folder, exist_ok=True)
        filename = f"security_report_{scan_id}.pdf"
        filepath = os.path.join(folder, filename)
        pdf.output(filepath)
        
        from flask import send_file
        return send_file(filepath, as_attachment=True, download_name=filename)
        
    except Exception as e:
        print(f"PDF Generation Error: {e}")
        return jsonify({'error': str(e)}), 500

