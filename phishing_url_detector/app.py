@app.route('/predict', methods=['POST'])
def predict():
    try:
        data = request.get_json()
        input_text = data.get('input', '').strip()
        input_type = data.get('type', 'auto')  # 'url', 'email', or 'auto'
        
        if not input_text:
            return jsonify({'error': 'Input is required'}), 400
            
        # 1. Determine Category (URL or Email)
        from model_utils import detect_input_type
        category = input_type.upper()
        if category == 'AUTO':
            category = detect_input_type(input_text).upper()
            
        print(f"🔍 Analyzing Input Category: {category}")

        # --- FLOW A: ADVANCED URL ANALYSIS (Visit/Crawler + WHOIS + AI) ---
        if category == 'URL':
            if url_model is None:
                return jsonify({'error': 'URL model not loaded'}), 500
            
            # STEP 1: AI Model Prediction
            ai_result = predict_smart(
                input_text, 
                url_model=url_model, 
                url_vectorizer=url_vectorizer,
                email_model=email_model,
                email_vectorizer=email_vectorizer
            )
            model_score = ai_result['confidence']
            
            # STEP 2: Domain WHOIS Intelligence
            try:
                domain_data = get_domain_info(input_text)
                domain_score = domain_data.get('risk_score', 0)
            except Exception as e:
                print(f"⚠️ WHOIS failed: {e}")
                domain_data = {}
                domain_score = 0
            
            # STEP 3: Webpage Behavior Crawler (The "Visit" component)
            try:
                print(f"🕷️ Running Crawler Analysis (SITE VISIT) on: {input_text}")
                # Ensure URL has scheme for Selenium
                crawler_url = input_text
                if not crawler_url.startswith(('http://', 'https://')):
                    crawler_url = 'http://' + crawler_url
                    
                behavior_result = analyze_webpage(crawler_url)
                behavior_score = behavior_result.get('behavior_score', 0)
                behavior_flags = behavior_result.get('flags', [])
                legitimacy_signals = behavior_result.get('legitimacy_signals', [])
            except Exception as e:
                print(f"⚠️ Crawler failed: {e}")
                behavior_score = 0
                behavior_flags = [f"Crawler check failed: {str(e)}"]
                legitimacy_signals = []
            
            # --- FINAL HYBRID SCORING ---
            # Standard weights: 60% model + 20% domain + 20% behavior
            weighted_score = (model_score * 0.6) + (domain_score * 0.2) + (behavior_score * 0.2)
            final_score = weighted_score
            
            # LEGITIMACY OVERRIDE: If the visit found strong company signals, lower the risk.
            if len(legitimacy_signals) >= 3:
                print(f"🛡️ Strong Legitimacy Signals found by Crawler. Lowering risk for URL.")
                final_score = min(weighted_score, 25) 
            elif len(legitimacy_signals) >= 2:
                final_score = min(weighted_score, 45) # Max "Suspicious"
                
            # If AI is highly confident AND no legitimacy signals were found, lean towards AI
            if model_score > 85 and len(legitimacy_signals) == 0:
                final_score = max(weighted_score, model_score * 0.9)
                
            # --- TRUSTED DOMAIN OVERRIDE ---
            TRUSTED_DOMAINS = {
                'openai.com', 'chatgpt.com', 'google.com', 'youtube.com', 'facebook.com', 
                'amazon.com', 'wikipedia.org', 'twitter.com', 'instagram.com', 'linkedin.com', 
                'netflix.com', 'microsoft.com', 'apple.com', 'github.com', 'stackoverflow.com', 
                'reddit.com', 'yahoo.com', 'bing.com', 'twitch.tv', 'whatsapp.com', 
                'adobe.com', 'salesforce.com', 'zoom.us', 'outlook.com', 'live.com',
                'nytimes.com', 'cnn.com', 'bbc.com', 'wsj.com', 'forbes.com', 'bloomberg.com',
                'medium.com', 'quora.com', 'dropbox.com', 'paypal.com', 'shopify.com',
                'phoneticedu.com'
            }
            
            # Clean domain extraction for whitelist check
            try:
                clean_url = input_text.replace('https://', '').replace('http://', '')
                domain_part = clean_url.split('/')[0]
                if domain_part.startswith('www.'): domain_part = domain_part[4:]
                if ':' in domain_part: domain_part = domain_part.split(':')[0]
            except:
                domain_part = ""

            is_trusted_domain = domain_part.lower() in TRUSTED_DOMAINS
            trusted_tlds = ('.ac.in', '.edu', '.gov', '.mil', '.org.in', '.nic.in')
            is_trusted_tld = domain_part.lower().endswith(trusted_tlds)
            
            suspicious_words_found = ai_result.get('suspicious_words', [])
            has_critical_keywords = any(w in suspicious_words_found for w in ['verify', 'account', 'login', 'update', 'payment'])
            
            if is_trusted_domain or (is_trusted_tld and not has_critical_keywords):
                 print(f"🛡️ Trusted Source detected: {domain_part}. Lowering risk.")
                 final_score = min(final_score, 15) 
                 recommendation = "Safe - Trusted / Verified Source"
            
            final_score = round(min(max(final_score, 0), 100), 2)
            
            # --- VERDICT DETERMINATION ---
            if final_score > 70:
                recommendation = "DO NOT VISIT - Highly Dangerous"
                prediction_label = "Phishing"
            elif final_score >= 40:
                recommendation = "Suspicious - Proceed Carefully"
                prediction_label = "Suspicious"
            else:
                recommendation = "Safe to Visit"
                prediction_label = "Legitimate"
            
            result = {
                'prediction': prediction_label,
                'confidence': final_score,
                'recommendation': recommendation,
                'scores': {
                    'ai_model': model_score,
                    'domain_risk': domain_score,
                    'behavior_risk': behavior_score,
                    'transformer': ai_result['scores'].get('transformer', 0),
                    'rule_based': ai_result['scores'].get('rule_based', 0),
                    'final': final_score
                },
                'domain_info': {
                    'creation_date': domain_data.get('creation_date', 'Unknown'),
                    'domain_age_days': domain_data.get('domain_age_days', 0),
                    'registrar': domain_data.get('registrar', 'Unknown')
                },
                'behavior_findings': behavior_flags,
                'legitimacy_signals': legitimacy_signals,
                'website_purpose': behavior_result.get('website_purpose', 'N/A'),
                'website_category': behavior_result.get('website_category', 'General'),
                'design_intent': behavior_result.get('design_intent', 'N/A'),
                'suspicious_words': list(set(ai_result.get('suspicious_words', []) + behavior_result.get('suspicious_words', []))),
                'input': input_text,
                'type': 'URL',
                'status': 'success'
            }

        # --- FLOW B: EMAIL ANALYSIS ---
        elif category == 'EMAIL':
            if email_model is None:
                return jsonify({'error': 'Email model not loaded'}), 500
            result = predict_email(input_text, model=email_model, vectorizer=email_vectorizer)
            recommendation = "Legitimate Email" if result['prediction'] == 'Legitimate' else "Potential Phishing Email"
            result['recommendation'] = recommendation

        else:
            return jsonify({'error': f'Category {category} not supported'}), 400

        
        # Generate unique scan ID and timestamp
        scan_id = str(uuid.uuid4())
        timestamp = datetime.datetime.now().strftime("%Y-%m-%d %H:%M:%S")
        
        # Add scan metadata to result
        result['scan_id'] = scan_id
        result['timestamp'] = timestamp
        
        # Store in history
        history_entry = {
            'id': scan_id,
            'input': input_text,
            'type': result.get('type', 'Unknown'),
            'prediction': result.get('prediction', 'Unknown'),
            'confidence': result.get('confidence', 0),
            'scores': result.get('scores', {}),
            'suspicious_words': result.get('suspicious_words', []),
            'domain_info': result.get('domain_info', {}),
            'recommendation': result.get('recommendation', 'N/A'),
            'behavior_findings': result.get('behavior_findings', []),
            'legitimacy_signals': result.get('legitimacy_signals', []),
            'website_purpose': result.get('website_purpose', 'N/A'),
            'website_category': result.get('website_category', 'General'),
            'design_intent': result.get('design_intent', 'N/A'),
            'timestamp': timestamp,
            'status': result.get('status', 'complete')
        }
        scan_history.insert(0, history_entry) # Add to beginning of list
        
        # Keep only last 50 entries
        if len(scan_history) > 50:
            scan_history.pop()
            
        return jsonify(result)
        
    except Exception as e:
        return jsonify({'error': str(e)}), 500

